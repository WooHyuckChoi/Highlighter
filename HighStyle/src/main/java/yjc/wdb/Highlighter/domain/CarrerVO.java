package yjc.wdb.Highlighter.domain;

public class CarrerVO {
	private String carrer_id;
	private String att_file;
	private String carrer_name;
	private String agen_name;
	private String user_id;
	
	public String getCarrer_id() {
		return carrer_id;
	}
	public void setCarrer_id(String carrer_id) {
		this.carrer_id = carrer_id;
	}
	public String getAtt_file() {
		return att_file;
	}
	public void setAtt_file(String att_file) {
		this.att_file = att_file;
	}
	public String getCarrer_name() {
		return carrer_name;
	}
	public void setCarrer_name(String carrer_name) {
		this.carrer_name = carrer_name;
	}
	public String getAgen_name() {
		return agen_name;
	}
	public void setAgen_name(String agen_name) {
		this.agen_name = agen_name;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	
	
}
